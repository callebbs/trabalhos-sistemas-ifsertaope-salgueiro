package aula002.pkg3;

// Estruturas de Repetição

// For

public class Aula0023 {

    public static void main(String[] args) {
    
        for(int i = 0; i <= 10; i++) {
            System.out.println( i );
        }
// While
        int i = 1;
        while( i <= 10 )
        {
            System.out.println( i );
            i++;
        }
        
// Do While
    int j = 1;
        do{
            System.out.println( j );
            j++;
        }while( j <= 10 );
    }
    
}
